  }

  String getByName(String name) {
    // Stupid case when emoji names aren't names but emojis themselves
    name = name.replaceAll(':', '');
    List<int> codePoints = [];
    String codePoint = _EMOJIS[name];
    if (codePoint == null) {
      if (reverseLookup(name) != null)
        return name;
      else {
        codePoint = _EMOJIS['question'];
      }
    }
    for (String cp in codePoint.split('-')) {
      codePoints.add(int.parse(cp, radix: 16));
    }
    return String.fromCharCodes(codePoints);
  }

  String reverseLookup(String value) {
    String hexcode =
        value.runes.map((r) => r.toRadixString(16).toUpperCase()).join('-');
    return _reversedEmojiMap[hexcode];
  }

